	<h3 style="font-family:Comic Sans MS; Text-decoration:underline">Eligibility</h3>
	<h4 style="line-height:24px;">To obtain M.B.B.S. admission the basic criteria is that a candidate should have scored more than 50% marks in 
	 physics, chemistry and biology aggregate in their 10+2 grade. To get medical school entry, one needs to appear 
	 for <b>Common Entrance Test (CET)</b> and <b>All India Medical Entrance Examination (AIMEE)</b>. The candidate needs to appear 
	 for All India Pre Medical Entrance Test for admission to 15% of the total seats for medical and dental courses 
	 in all medical and dental colleges run by the Union of India. This test is conducted by Central board of 
	 Secondary education.
	 <br><br>
	 A basic requirement for applying for medical entrance tests is that the student should be <b>at least 17</b> years old 
	 on or before 31st December of the year of admission. Upper age limit, where specified, is <b>25 years</b>. 
	 Typically, Written tests have questions from physics, chemistry and biology (botany and zoology). In general, 
	 the mix of questions is such that the focus on biology, which is a combination of zoology and botany, is as much 
	 as that on physics and chemistry together.
	 <br><br>
     Most of the time there is negative marking for a wrong answer. This would be indicated in the information 
	 bulletin or prospectus. Some medical entrance tests also have a few questions on general knowledge, personality 
	 and intelligence assessment or English language. The states also conduct their own Pre Medical Entrance 
	 Examination for domiciled candidates.<br><br>
